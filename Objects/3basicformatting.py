#Basic formatting
#Strings also support an advanced
#substitution operation known as formatting, available as both an #expression (the orignal) and a string method call

print('%s, eggs, and %s' % ('paratha', 'CHEESE!')) #formatting expression
print('{0}, eggs, and {1}'.format('paratha','CHEESE!')) #formatting method
print('{}, eggs, and {}'.format('paratha','CHEESE!')) #numbers optional

